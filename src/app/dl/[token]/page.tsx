export default async function CanvasRedirectPage({
  params,
}: {
  params: { token: string };
}) {
  const { token } = params;

  console.log('ğŸ”— [Redirect] Canvas redirect requested for token:', token);

  try {
    // Query database for the token
    const { data, error } = await supabase
      .from('canvas_orders')
      .select('public_url')
      .eq('token', token)
      .single();

    if (error || !data) {
      console.error('âŒ [Redirect] Token lookup error:', error);
      console.error('Token:', token);
      // Redirect to error page or 404
      redirect('/404');
    }

    console.log('âœ… [Redirect] Token found, redirecting to:', data.public_url.substring(0, 50) + '...');

    // Redirect to the actual Supabase public URL
    redirect(data.public_url);

  } catch (error) {
    console.error('âŒ [Redirect] Unexpected error:', error);
    redirect('/404');
  }
}

